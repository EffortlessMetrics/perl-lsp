#!/usr/bin/perl

# Arithmetic operators
my $sum = 5 + 3;
my $diff = 10 - 4;
my $product = 6 * 7;
my $quotient = 20 / 4;
my $power = 2 ** 3;

# String concatenation
my $string = "Hello" . " " . "World";

# Range operator
my @numbers = (1..10);

# Logical operators
my $result = $foo || $bar;
my $check = $x && $y;

# Comparison
my $equal = $a == $b;
my $not_equal = $x != $y;
my $less_than = $i < 10;
my $greater = $j > 5;

# Complex expressions
my $complex = $x + $y * $z;
my $with_parens = ($a + $b) * $c;

# Fat comma
my %hash = (
    name => "John",
    age => 30
);